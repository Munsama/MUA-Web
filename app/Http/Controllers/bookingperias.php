     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $insert = booking::create([
            'nama_perias' => $request->nama_perias,
            'jenis_rias' => $request->jenis_rias,
            'harga' => $request->harga,
            'alamat' => $request->alamat,
            'tanggal' => $request->tanggal,
            'uname'=>$request->uname
        ]);

        if($insert)
        return redirect()->back()->with("success","Permintaan anda telah terkirim");
        else {
            return redirect()->back()->with("error","Gagal mengirim permintaan");
        }
    }

